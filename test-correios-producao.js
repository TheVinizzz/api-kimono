// Fun√ß√£o para autenticar na API
async function autenticar() {
  try {
    console.log('üîë Autenticando na API dos Correios...');
    console.log(`üîß Ambiente: ${CORREIOS_CONFIG.ambiente}`);
    console.log(`üîß URL Base: ${apiClient.defaults.baseURL}`);
    
    // Criar credenciais em formato Base64
    const credentials = Buffer.from(`${CORREIOS_CONFIG.idCorreios}:${CORREIOS_CONFIG.codigoAcesso}`).toString('base64');
    
    // Tentar autentica√ß√£o com cart√£o de postagem
    try {
      console.log(`üîë Tentando autentica√ß√£o com cart√£o de postagem: ${CORREIOS_CONFIG.cartaoPostagem}`);
      
      const response = await axios({
        method: 'post',
        url: `${apiClient.defaults.baseURL}${ENDPOINTS.token.autenticaCartaoPostagem}`,
        headers: {
          'Content-Type': 'application/json',
          'Accept': 'application/json',
          'Authorization': `Basic ${credentials}`
        },
        data: {
          numero: CORREIOS_CONFIG.cartaoPostagem
        },
        timeout: 30000
      });
      
      if (response.data && response.data.token) {
        token = response.data.token;
        tokenExpiration = new Date(response.data.expiraEm);
        
        console.log('‚úÖ Token obtido com sucesso (cart√£o de postagem)');
        console.log(`‚úÖ Token expira em: ${tokenExpiration.toLocaleString()}`);
        console.log(`‚úÖ Ambiente retornado: ${response.data.ambiente || 'N/A'}`);
        
        // Configurar interceptor para adicionar token
        apiClient.interceptors.request.use(config => {
          config.headers.Authorization = `Bearer ${token}`;
          return config;
        });
        
        return true;
      }
    } catch (cartaoError) {
      console.error('‚ö†Ô∏è Falha na autentica√ß√£o com cart√£o de postagem:', cartaoError.message);
      
      if (cartaoError.response) {
        console.error('‚ö†Ô∏è Status da resposta:', cartaoError.response.status);
        console.error('‚ö†Ô∏è Dados da resposta:', JSON.stringify(cartaoError.response.data || {}));
      }
      
      console.log('üîÑ Tentando m√©todo de autentica√ß√£o alternativo...');
    }
    
    // Autentica√ß√£o direta
    const response = await axios({
      method: 'post',
      url: `${apiClient.defaults.baseURL}${ENDPOINTS.token.autentica}`,
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json',
        'Authorization': `Basic ${credentials}`
      },
      timeout: 30000
    });
    
    if (response.data && response.data.token) {
      token = response.data.token;
      tokenExpiration = new Date(response.data.expiraEm);
      
      console.log('‚úÖ Token obtido com sucesso');
      console.log(`‚úÖ Token expira em: ${tokenExpiration.toLocaleString()}`);
      console.log(`‚úÖ Ambiente retornado: ${response.data.ambiente || 'N/A'}`);
      
      // Configurar interceptor para adicionar token
      apiClient.interceptors.request.use(config => {
        config.headers.Authorization = `Bearer ${token}`;
        return config;
      });
      
      return true;
    } else {
      console.error('‚ùå Resposta sem token v√°lido');
      console.error('‚ùå Dados da resposta:', JSON.stringify(response.data || {}));
      return false;
    }
  } catch (error) {
    console.error('‚ùå Erro na autentica√ß√£o:', error.message);
    
    if (error.response) {
      console.error('‚ùå Status da resposta:', error.response.status);
      console.error('‚ùå Dados da resposta:', JSON.stringify(error.response.data || {}));
      
      if (error.response.status === 401) {
        console.error('‚ùå Credenciais inv√°lidas. Verifique ID e c√≥digo de acesso.');
      }
    }
    
    return false;
  }
}

